package com.ylw.mybatis.entity;

public class User {



	private String NAME;
	private Integer age;

	public User() {
	}

	public String getNAME() {
		return NAME;
	}

	public void setNAME(String NAME) {
		this.NAME = NAME;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
}
